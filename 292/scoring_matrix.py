class AminoAcidNotFoundError(Exception):
    pass


def matrix_score(sequence1: str, sequence2: str, matrix_str: str = BLOSUM62) -> int:
    """
    Receives two proteins sequences and a matrix table
    Returns the score of two proteins according to the supplied matrix table
    """
    scores = []
    df = matrix_to_df(matrix_str)
    s1 = [char.upper() for char in sequence1]
    s2 = [char.upper() for char in sequence2]
    for idx, s in enumerate(s1):
        try:
            score = df.loc[s, s2[idx]]
        except KeyError:
            raise AminoAcidNotFoundError(f"Scoring matrix does not support scoring for: ('{s}', '{s2[idx]}')")
        scores.append(int(score))
    return sum(scores)


def closest_match(
    reference_sequence: str, query_sequences: List[str], matrix_str: str = BLOSUM62
) -> Union[str, List, None]:
    """
    Receives a reference sequence, a list of query sequences and a matrix table
    Returns the closest matching sequence(s) or None
    """
    if not query_sequences:
        return None
    sequence_scores = [(idx, matrix_score(reference_sequence, s, matrix_str)) for idx,s in enumerate(query_sequences)]
    closest_matching = max(sequence_scores, key=lambda x: x[1])
    ties = [s for s in sequence_scores if closest_matching[1] == s[1]]
    if len(ties) > 1:
        return [query_sequences[tie[0]] for tie in ties]
    return query_sequences[closest_matching[0]]


def matrix_to_df(matrix_str) -> pd.DataFrame:
    raw_matrix = [line for line in matrix_str.splitlines() if not line.startswith("#")]
    new_matrix = []
    columns = raw_matrix[0].strip().split()
    for line in raw_matrix[1:]:
        line_split = line.split()
        new_matrix.append(line_split[1:])
    df = pd.DataFrame(new_matrix, columns=columns, index=columns)
    return df
